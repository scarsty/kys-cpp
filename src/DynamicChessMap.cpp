std::shared_ptr<BattleSceneHades> DynamicChessMap::createBattle(const DynamicBattleRoles& roles)
{
    // Select map that has enough enemy positions for the requested enemies
    const auto& maps = getTopMaps();
    int needed = (int)roles.enemy_ids.size();
    std::vector<int> candidates;
    for (int i = 0; i < (int)maps.size(); i++)
    {
        if (maps[i].enemy_count >= needed)
            candidates.push_back(i);
    }
    // Fallback: if no map has enough, pick the one with the most enemies
    if (candidates.empty())
    {
        int best = 0;
        for (int i = 1; i < (int)maps.size(); i++)
        {
            if (maps[i].enemy_count > maps[best].enemy_count)
                best = i;
        }
        candidates.push_back(best);
    }
    int map_index = candidates[KysChess::GameData::get().enemyRandInt(candidates.size())];
    const auto& selected_map = maps[map_index];

    // Create battle scene
    auto battle = std::make_shared<BattleSceneHades>();
    battle->setID(selected_map.battle_id);
    battle->setNoExp();

    // Prepare extended teammate info (up to 10 teammates)
    std::vector<ExtendedTeammateInfo> teammates;

    // Combine existing positions (6) and new positions (4)
    std::vector<std::pair<int, int>> all_positions = selected_map.existing_positions;
    all_positions.insert(all_positions.end(),
                        selected_map.new_positions.begin(),
                        selected_map.new_positions.end());

    // Fill in teammate positions
    for (size_t i = 0; i < roles.teammate_ids.size() && i < all_positions.size(); i++)
    {
        ExtendedTeammateInfo info;
        info.ID = roles.teammate_ids[i];
        info.X = all_positions[i].first;
        info.Y = all_positions[i].second;
        info.star = (i < roles.teammate_stars.size()) ? roles.teammate_stars[i] : 1;
        teammates.push_back(info);
    }

    battle->setExtendedBattleInfo(teammates);
    battle->setEnemyStars(roles.enemy_stars);

    // Override enemy IDs using modifiableInfo
    auto& info = battle->modifiableInfo();
    for (size_t i = 0; i < roles.enemy_ids.size() && i < BATTLE_ENEMY_COUNT; i++)
    {
        info.Enemy[i] = roles.enemy_ids[i];
    }
    // Set remaining enemy slots to -1
    for (size_t i = roles.enemy_ids.size(); i < BATTLE_ENEMY_COUNT; i++)
    {
        info.Enemy[i] = -1;
    }

    return battle;
}

