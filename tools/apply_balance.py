def apply(db_path):
    conn = sqlite3.connect(db_path)

    # Apply skill power curve buffs
    for sid, powers in SKILL_BUFFS.items():
        cols = ", ".join(f"威力{i+1}=?" for i in range(10))
        conn.execute(f"UPDATE magic SET {cols} WHERE 编号=?", powers + [sid])
    conn.commit()
    print(f"Buffed {len(SKILL_BUFFS)} skill power curves")

    for rid, lvl, hp, atk, spd, def_, mp, s0id, s0lvl, s1id, s1lvl, fist, sword, knife, unusual, hidden in BALANCE:
        params = [lvl, hp, hp, atk, spd, def_, mp, mp,
                  fist, sword, knife, unusual, hidden,
                  s0id, s0lvl, s1id, s1lvl]
        # Clear skills 3-10
        for _ in range(8):
            params.extend([0, 0])
        params.append(rid)

        skills_set = ", ".join(
            f"所会武功{i+1}=?, 武功等级{i+1}=?" for i in range(MAGIC_COUNT))
        sql = (f"UPDATE role SET 等级=?, 生命=?, 生命最大值=?, "
               f"攻击力=?, 轻功=?, 防御力=?, 内力=?, 内力最大值=?, "
               f"拳掌=?, 御剑=?, 耍刀=?, 特殊=?, 暗器=?, "
               f"{skills_set} WHERE 编号=?")
        conn.execute(sql, params)

    conn.commit()
    print(f"Applied balance to {len(BALANCE)} roles in {db_path}")
    conn.close()

def apply_zip(zip_path):
    with zipfile.ZipFile(zip_path, 'r') as z:
        if '1.db' not in z.namelist():
            return
        names = z.namelist()
        with tempfile.TemporaryDirectory() as tmp:
            z.extractall(tmp)
            apply(os.path.join(tmp, '1.db'))
            tmp_zip = zip_path + '.tmp'
            with zipfile.ZipFile(tmp_zip, 'w', zipfile.ZIP_DEFLATED) as zout:
                for name in names:
                    zout.write(os.path.join(tmp, name), name)
            shutil.move(tmp_zip, zip_path)
    print(f"Updated zip: {zip_path}")

